class BloodBankEntry{
    constructor(id,name,age,bldgrp,contact,qty,collectionDate,expirationDate,status){
        this.id = id;
        this.name = name;
        this.age = age;
        this.bldgrp = bldgrp;
        this.contact = contact;
        this.qty = qty;
        this.collectionDate = collectionDate;
        this.expirationDate = expirationDate;
        this.status = status;
    }
}